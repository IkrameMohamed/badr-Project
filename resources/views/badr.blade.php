               <div class="about_text">
                  <p>Creee en 2006 a Blida, l'association EL-BADR est une association d'aide aux malades atteints de cancer. Elle intervient pour aider les patients et leurs proches a vaincre la maladie cancereuse et traverser cette epreuve. Ses membres sont reconnus d'utilite publique reposant sur la generosite des medecins, du grand public et sur l'engagement de ses benevoles.
Afin d'informer, d'accompagner, et d'aider les malades atteints de cancer l'association EL-BADR a introduit un ensemble de services, pour lutter efficacement contre le cancer, et ameliorer la qualite de vie des personnes fragilisees par la maladie.
Aussi l'association EL-BADR mene regulierement des campagnes de sensibilisation sur les facteurs de risques des cancers, les moyens de depistage et de diagnostic precoce des cancers. </p>
                   <h2><span>De l'accompagnement </span> au quotidien</h2>
              <p>Grace a la generosite de ses benevoles et donateurs, l'association EL-BADR aide et accompagne les personnes malades et leurs proches, et aussi sensibilise et informe la population sur les differents cancer, les moyens de depistage et les facteurs de risque.</p>

